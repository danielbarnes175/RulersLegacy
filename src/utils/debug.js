const debug = true;

export const log = (object) => {
  return debug ? JSON.stringify(object, getCircularReplacer()) : "";
};

const getCircularReplacer = () => {
  const seen = new WeakSet();
  return (key, value) => {
    if (typeof value === "object" && value !== null) {
      if (seen.has(value)) {
        return;
      }
      seen.add(value);
    }
    return value;
  };
};
